CREATE TABLE public.raw_webhook_logs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at timestamp with time zone DEFAULT now() NOT NULL,
  payload jsonb
);

ALTER TABLE public.raw_webhook_logs ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow insert for service" ON public.raw_webhook_logs FOR INSERT WITH CHECK (true);
CREATE POLICY "Admins can read raw_webhook_logs" ON public.raw_webhook_logs FOR SELECT USING (is_admin());